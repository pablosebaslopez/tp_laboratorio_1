#ifndef FUNCIONES_H_INCLUDED
#define FUNCIONES_H_INCLUDED

/**
 * \brief Realiza la suma entre dos numeros
 * \param num1 es el primer numero que recibe
 * \param num2 es el segundo numero que recibe
 * \param cont es la cantidad de operandos que recibe la operacion
 * \return void - no retorna nada
 *
 */
void suma(float num1, float num2, int cont);

/**
 * \brief Realiza la resta entre dos numeros
 * \param num1 es el primer numero que recibe
 * \param num2 es el segundo numero que recibe
 * \param cont es la cantidad de operandos que recibe la operacion
 * \return void - no retorna nada
 *
 */

void resta(float num1, float num2, int cont);

/**
 * \brief Realiza la division entre dos numeros
 * \param num1 es el primer numero que recibe
 * \param num2 es el segundo numero que recibe
 * \param cont es la cantidad de operandos que recibe la operacion
 * \return void - no retorna nada
 *
 */
void division(float num1, float num2, int cont);

/**
 * \brief Realiza la multiplicacion entre dos numeros
 * \param num1 es el primer numero que recibe
 * \param num2 es el segundo numero que recibe
 * \param cont es la cantidad de operandos que recibe la operacion
 * \return void - no retorna nada
 *
 */

void multiplicacion(float num1, float num2, int cont);

/**
 * \brief Realiza el factorial de un numero
 * \param num1 es el numero que recibe
 * \return void - no retorna nada
 *
 */

void factorial(float num1);



#endif // FUNCIONES_H_INCLUDED
